export const getCommitteeByYear = (year) => {
  return coreCommitteeData[year] || [];
};

export const getAvailableYears = () => {
  return Object.keys(coreCommitteeData).sort((a, b) => b - a);
};

export const getAllCommitteeMembers = () => {
  const allMembers = [];
  Object.keys(coreCommitteeData).forEach((year) => {
    coreCommitteeData[year].forEach((member) => {
      allMembers.push({ ...member, year });
    });
  });
  return allMembers;
};

export const searchCommitteeMembers = (searchTerm) => {
  const results = [];
  Object.keys(coreCommitteeData).forEach((year) => {
    const yearResults = coreCommitteeData[year].filter((member) =>
      member.name.toLowerCase().includes(searchTerm.toLowerCase())
    );
    yearResults.forEach((member) => {
      results.push({ ...member, year });
    });
  });
  return results;
};

export const getMembersByRole = (role, year = null) => {
  const members = [];
  const years = year ? [year] : Object.keys(coreCommitteeData);

  years.forEach((yr) => {
    if (coreCommitteeData[yr]) {
      const filtered = coreCommitteeData[yr].filter((member) =>
        member.designation.toLowerCase().includes(role.toLowerCase())
      );
      filtered.forEach((member) => {
        members.push({ ...member, year: yr });
      });
    }
  });

  return members;
};
